export function getFeaturedPaintings() {
  return paintings.filter((p) => p.Featured)
}

export function getPaintingsByStyle(style) {
  if (!style || style === 'All') return paintings
  return paintings.filter((p) => p.Style === style)
}

export function getPaintingById(id) {
  return paintings.find((p) => p.ID === Number(id))
}

